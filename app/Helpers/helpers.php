    public static function formatFilenameSecond($filename)
    {
        return time() . '_' . preg_replace('/[^a-zA-Z0-9._-]/', '', $filename);
    }

    public static function updatePageConfig($pageConfigs)
    {
        $demo = 'custom';
        $fullURL = request()->fullurl();
        if (App()->environment() === 'production') {
            for ($i = 1; $i < 7; $i++) {
                $contains = Str::contains($fullURL, 'demo-' . $i);
                if ($contains === true) {
                    $demo = 'demo-' . $i;
                }
            }
        }
        if (isset($pageConfigs)) {
            if (count($pageConfigs) > 0) {
                foreach ($pageConfigs as $config => $val) {
                    FacadesConfig::set('custom.' . $demo . '.' . $config, $val);
                }
            }
        }
    }

    public static function getAuthConfig()
    {
        if (!Auth::user()) {
            $user = User::with('accountType')->find(1);
        } else {
            $user = Auth::user();
        }
        $account_type = $user->accountType ?? AccountType::find($user->id_account_type);
        return ['user' => $user, 'account_type' => $account_type];
    }

    /**
     * Clean the filename with his own name and extension
     */
    public static function formatFilename($string)
    {
        $filename = pathinfo($string, PATHINFO_FILENAME); // Get filename without extension
        $extension = pathinfo($string, PATHINFO_EXTENSION); // Get file extension
        $clean_string = strtolower(str_replace(" ", "_", $filename));
        $pretty_name = preg_replace('/[^A-Za-z0-9_\-]/', '', $clean_string) . '.' . $extension;
        return $pretty_name;
    }

    public static function replaceAccents($cadena)
    {

        //Reemplazamos la A y a
        $cadena = str_replace(
            array('Á', 'À', 'Â', 'Ä', 'á', 'à', 'ä', 'â', 'ª'),
            array('A', 'A', 'A', 'A', 'a', 'a', 'a', 'a', 'a'),
            $cadena
        );

        //Reemplazamos la E y e
        $cadena = str_replace(
            array('É', 'È', 'Ê', 'Ë', 'é', 'è', 'ë', 'ê'),
            array('E', 'E', 'E', 'E', 'e', 'e', 'e', 'e'),
            $cadena
        );

        //Reemplazamos la I y i
        $cadena = str_replace(
            array('Í', 'Ì', 'Ï', 'Î', 'í', 'ì', 'ï', 'î'),
            array('I', 'I', 'I', 'I', 'i', 'i', 'i', 'i'),
            $cadena
        );

        //Reemplazamos la O y o
        $cadena = str_replace(
            array('Ó', 'Ò', 'Ö', 'Ô', 'ó', 'ò', 'ö', 'ô'),
            array('O', 'O', 'O', 'O', 'o', 'o', 'o', 'o'),
            $cadena
        );

        //Reemplazamos la U y u
        $cadena = str_replace(
            array('Ú', 'Ù', 'Û', 'Ü', 'ú', 'ù', 'ü', 'û'),
            array('U', 'U', 'U', 'U', 'u', 'u', 'u', 'u'),
            $cadena
        );

        //Reemplazamos la N, n, C y c
        $cadena = str_replace(
            array('Ñ', 'ñ', 'Ç', 'ç'),
            array('N', 'n', 'C', 'c'),
            $cadena
        );

        return $cadena;
    }

    /**
     * Make pretty name without special characters
     */
    public static function formatToFolderName($string)
    {
        return strtolower(str_replace(" ", "_", self::replaceAccents($string)));
    }

